#define SZ1 2000010000
#define SZ2 157714560

char sieve[(SZ1>>4)+7];
int prime[SZ2];

void bit_sieve()
{
    int i,j,k,r;
    prime[0]=2;
    k=1;
    int lim=(int)sqrt(SZ1)+1;
    int pr=2,mxd=1;
    for(i=3; i<SZ1; i+=2)
    {
        if(!(sieve[i>>4]&(1<<((i>>1)&7))))
        {
            prime[k++]=i;
            mxd=max(mxd,i-pr);
            pr=i;
            if(i<lim)
            {
                r=i<<1;
                for(j=i*i; j<SZ1; j+=r)
                {
                    sieve[j>>4]|=(1<<((j>>1)&7));
                }
            }
        }
    }
    deb(k,mxd);
    return;
}

int main(void)
{
    bit_sieve();
    return 0;
}
